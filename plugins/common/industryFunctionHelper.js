industryFunctionHelper.setwftRegionUlLi = function(elemCity, elemProvince, elemCityBtn, elemProvinceBtn) {
	//加载省
	$(elemProvince).empty();
	$(elemCity).empty();
	$.each(dataIndustryProvince, function(index, entry) {
		$(elemProvince).append("<li value=" + entry.code + "><a>" + entry.name + "</a></li>");
	});
	//省选择事件
	$(elemProvince).find('li').click(function() {
		//选择市
		$(elemProvinceBtn).attr('data-value', $(this).attr('value'));
		$(elemProvinceBtn).html($(this).find('a').text());
		$(elemCityBtn).html('市');
		$(elemCityBtn).attr('data-value', '');
		$(elemCity).empty();
		var cityParentId = $(this).attr('value');
		$.each(dataIndustryCity, function(index1, entry1) {
			if(entry1.supCode == cityParentId) {
				$(elemCity).append("<li value=" + entry1.code + " parentId=" + entry1.supCode + "><a>" + entry1.name + "</a></li>");
			}
		});
		//市选择事件
		$(elemCity).find('li').click(function() {
			//选择县
			$(elemCityBtn).attr('data-value', $(this).attr('value'));
			$(elemCityBtn).html($(this).find('a').text());
		});

	});

};

